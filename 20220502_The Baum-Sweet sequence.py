from itertools import count
def baum_sweet():
    yield 1
    for i in count(1):
        yield not any(map(lambda x: len(x)%2, bin(i).split('1')))

def baum_sweet1():
    yield 1
    i = 0
    while True:
        i += 1
        yield not any(map(lambda j: len(j)%2, bin(i).split('1')))

